import random
print(random.randint(5, 20))  # line 1
# I saw whole numbers between 5 and 20, where the smallest number I could have seen was 5 and the largest was 20.
print(random.randrange(3, 10, 2))  # line 2
# I saw random whole odd numbers between 3 and 10, with the lowest number being 3 and the highest being 9.
print(random.uniform(2.5, 5.5))  # line 3
# I saw random float values between 2.5 and 5.5, with the smallest number being 2.5 and the largest being 5.5.
print(random.randint(1, 100))
